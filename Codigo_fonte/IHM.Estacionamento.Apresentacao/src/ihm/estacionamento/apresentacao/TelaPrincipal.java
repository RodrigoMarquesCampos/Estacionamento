    public TelaPrincipal(Usuario p) {
        
        setExtendedState(MAXIMIZED_BOTH);
        
        if(!Aplicacao.isLogged()){
            if(telalogin == null)
                telalogin = new TelaLogin();
            
            telalogin.setVisible(true);
            this.setVisible(false);
            this.p = p;
            return;          
        }
        
        initComponents();
        if(p.getTipo().getId() == 2)
            mnuUsuarios.setVisible(false);
    }
    
